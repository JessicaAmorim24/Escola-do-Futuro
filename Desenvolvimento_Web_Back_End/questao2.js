// Questão 2 - Cálculo de Média Escolar
// Maria tirou a seguintes notas em 3 provas: 7.5, 8.0 e 9.0.
// Calcule a média das notas de Maria.

let nota1 = 7.5
let nota2 = 8.0
let nota3 = 9.0

let media = (nota1 + nota2 + nota3)/3

console.log(`Média: ${media}`)

// Ou também pode ser desta forma:

let notas = [7.5, 8.0, 9.0]

let media2 = (notas[0] + notas[1] + notas[2])/3

console.log(`Média: ${media2}`)